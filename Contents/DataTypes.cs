namespace StudiesCSharp.Contents
{
    public class DataTypes
    {
        #region Numbers Types
            // signed types
            byte b; // (8 bit) - 0 to 255
            short s; // (16 bit) - -32768 to 32767
            int a; // (32 bit) - -2147483648 to 2147483647
            long l; // (64 bit) - -9223372036854775808 to 9223372036854775807


            // unsigned types
            ushort us; // (16 bit) - 0 to 65.535
            uint ua; // (32 bit) - 0 to 4.294.967.295
            ulong ul; // (64 bit) - 0 to 18.446.744.073.709.551.615
        #endregion
        
        #region Characters Types
            char firstCharOfMyName = 'Y';
            string str = "Hello, my name is Yuri";
        #endregion

    }
}